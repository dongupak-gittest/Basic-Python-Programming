# 파이썬의 연산자에 대해 알아보는 실습 코드입니다
a = 33     # 변수 a 에 33을 할당함
b = 3      # 변수 b 에 3을 할당함

num1 = a + b    # 두 변수의 합을 구함
num2 = a * b    # 두 변수의 곱을 구함
num3 = a / b    # a를 b로 나눔
num4 = a % b    # a를 b로 나누어 나머지를 구함
num5 = a ** b   # a를 b 제곱함, 여기서는 33의 3제곱

# 위의 연산 결과를 출력함
print(num1, num2, num3, num4, num5)


